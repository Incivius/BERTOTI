public class Localização {
    int rua;
    int andar;
    
    public Localização(int rua, int andar){
        this.rua = rua;
        this.andar = andar;
    }
    public int getRua(){
        return rua;
    }
    public void setRua(int rua){
        this.rua = rua;
    }
    public int getAndar(){
        return andar;
    }
    public void setAndar(int andar){
        this.andar = andar;
    }
}
